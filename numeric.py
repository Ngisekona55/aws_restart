print("Python has three numeric types: int, float, and complex")
myValue = 1
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

myFloatValue = 3.14
print(myFloatValue)
print(str(myFloatValue) + " is of the data type " + str(type(myFloatValue)))

myComplexValue = 5j
print(myComplexValue)
print(type(myComplexValue))
print(str(myComplexValue) + " is of the data type " + str(type(myComplexValue)))

myBoolDataValue = True
print(myBoolDataValue)
print(type(myBoolDataValue))
print(str(myBoolDataValue) + " is of the data type " + str(type(myBoolDataValue)))
